export default function PixelIcon({ icon, size = 24, className = '', style, ...props }) {
  const grid = ICON[icon]
  if (!grid) return null
  const n = grid.length
  return (
    <svg
      viewBox={`0 0 ${n} ${n}`}
      width={size}
      height={size}
      className={className}
      style={{ shapeRendering: 'crispEdges', ...style }}
      fill="currentColor"
      aria-hidden="true"
      {...props}
    >
      {grid.flatMap((row, y) => row.split('').map((c, x) => (c === '1' ? <rect key={`${x}-${y}`} x={x} y={y} width="1" height="1" /> : null)))}
    </svg>
  )
}
